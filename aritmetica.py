 
# '//' Retorna o número interiro de uma divisão sem a parte fracionária
print (10 // 9)
print (10 / 9)

# '%' Retorna o resto da divisão
print ("Resto:", 4 % 2)
print ("Resto:", 5 % 2)

# FUNÇÕES MATEMÁTICA
# abs() Retorna o valor absoluto de um número
print (abs(-10))

# min() Retorna o valor mínimo
print (min(50, -8, 105))

# max() Retorna o valor maior
print (max(50, -8, 105))


